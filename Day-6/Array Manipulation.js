// 1. Array Manipulation Script: Write a script that demonstrates the creation of an array, adding and removing elements 
// using push, pop, shift, and unshift methods.

let arr=[1,2,10,20,40]

arr.push(50)

console.log("Updated Array after push:",arr)


arr.pop()

console.log("Updated Array after pop:",arr)


arr.shift()

console.log("Updated Array after shift:",arr)


arr.unshift(5)

console.log("Updated Array after unshift:",arr)
