// 1. Variable Types Console Log: Write a script that declares variables of different data types and logs both the
// value and type of each variable to the console.


let num1 = 1000
console.log("number",num1,typeof(num1))

let str1 = "code"
console.log("string",str1,typeof(str1))

let bool1 = true
console.log("boolean",bool1,typeof(bool1))

let obj1 = {
    "key":"count",
    "value":1
}
console.log("Object",obj1,typeof(obj1))

let arr = [1,2,3,4,5]
console.log("Array",arr,typeof(arr))